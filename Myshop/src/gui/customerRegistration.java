    public void loadCustomer() {

        try {
            ResultSet rs = MySQL.search("SELECT `customer`.`id`,`customer`.`name`,`customer`.`contact_number`,`customer`.`address`,`customer`.`nic`,`city`.`name` AS `city_name`,`gender`.`name` AS `gender_name` FROM `customer`INNER JOIN `city` ON `customer`.`city_id` = `city`.`id` INNER JOIN `gender` ON `customer`.`gender_id` = `gender`.`id` ORDER BY `customer`.`id` ASC");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("contact_number"));
                v.add(rs.getString("address"));
                v.add(rs.getString("nic"));
                v.add(rs.getString("city_name"));
                v.add(rs.getString("gender_name"));
                dtm.addRow(v);
            }
            jTable1.setModel(dtm);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

